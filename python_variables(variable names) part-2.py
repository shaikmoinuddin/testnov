# ### rules to name a variable:

# + a variable name must start with a name or underscore(_) charecter.
# + a variable cannot start with a number.
# + a variable name can only contain alpha-numeric charecters and underscores(_)(A-z, 0-9, and _ ).
# + variable names are case-sensitive(age, Age and AGE are three different variables).

# In[ ]:


# example:

myvar = "John"
my_var = "John"
_my_var = "John"
myVar = "John"
MYVAR = "John"
myvar2 = "John"


# ### Multi Words Variable Names:

# + variables with more than one word is difficult to read.
# + there are several techniques we can use to make them more readable.
# + 1.camel case
# + 2.pascal case
# + 3.snake case

# ### 1.Camel Case:

# + except first word,each word starts with capital letter.

# In[2]:


# example:

myVariableName = "John"


# ### Pascal Case:

# + each word starts with capital letter.

# In[3]:


# example:

MyVariableName = "John"


# ### Snake Case:

# + each word is seperated by an underscore(_)

# In[ ]:


# example:

my_variable_name = "John"

